void print_version() {
    printf("micody version 1.0\n");
}

void print_help() {
    printf("\n\nmicody Help\n");
    printf("Use : micody [options] or  micody <filename>\n");
    printf("Options:\n");
    printf("  -v        				display the version of micody\n");
    printf("  -h        				display help \n");
}

